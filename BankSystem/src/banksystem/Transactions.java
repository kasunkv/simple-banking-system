    private void btnDM_DepositeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDM_DepositeActionPerformed
        String accNumber = txtDM_AccNo.getText();
        String accountStatus = null;
        String dipMethod = null;
        float depositAmount = Float.parseFloat(txtDM_Amount.getText());
        float accBalance = 0;
        float newBalance = 0;
        
        try {
            Connection dbConnect = DBConnection.connect();
            Statement dbQuery = dbConnect.createStatement();
            ResultSet result = dbQuery.executeQuery("SELECT accStatus, balance FROM accounts WHERE accNo = '"+accNumber+"';");
            
            while(result.next())
            {
                accBalance = result.getFloat("balance");
                accountStatus = result.getString("accStatus");
            }
            
            if (accountStatus.equals("Deactive"))
            {
                JOptionPane.showMessageDialog(null, "Account Number : " + accNumber + "\nThe Account is deactivated.\nTransactions can not performe on this Account.", "Deposit Money", JOptionPane.INFORMATION_MESSAGE);
                this.clearDMTextFields();
            }
            else if (accountStatus.equals("Active")) 
            {
                newBalance = accBalance + depositAmount;
                txtDM_NewBal.setText("Rs." + String.valueOf(newBalance));
                txtDM_PrevBal.setText("Rs." +String.valueOf(accBalance));               
                
                if (optCash.isSelected())
                    dipMethod = "Cash";
                else if (optCheque.isSelected())
                    dipMethod = "Cheque";
                
                SavingsAccount newSA = new SavingsAccount();
                
               if (newSA.deposit(depositAmount, accNumber, dipMethod))
               {
                   JOptionPane.showMessageDialog(null, "Account updated successfully.", "Deposit Money", JOptionPane.INFORMATION_MESSAGE);
               }
               else
               {
                   JOptionPane.showMessageDialog(null, "Account updated Falied.", "Deposit Money", JOptionPane.ERROR_MESSAGE);
                   this.clearDMTextFields();
               }
                   
                
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Update failed in 'Transaction Database'\nError Message : " + e.getMessage(), "Deposit Money", JOptionPane.ERROR_MESSAGE);
        }
        
        try {
            Connection dbConnect = DBConnection.connect();
            PreparedStatement dbUpdate = dbConnect.prepareStatement("UPDATE accounts SET balance = '"+newBalance+"' WHERE accNo = '"+accNumber+"'");
            dbUpdate.executeUpdate();
            dbUpdate.close();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Update failed in 'Accounts Database'\nError Message : " + e.getMessage(), "Deposit Money", JOptionPane.ERROR_MESSAGE);
        }
               
                
        
    }//GEN-LAST:event_btnDM_DepositeActionPerformed

