    private void btnAddEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddEmpActionPerformed
        String post = null;
        String userName = txtUsername.getText();
        String password = String.valueOf(pswPass.getPassword());
        String passwordConfirm = String.valueOf(pswConfirmPass.getPassword());
                       
        if (optAdmin.isSelected())
            post = "admin";
        else if (optOper.isSelected())
            post = "oper";
        
        Employee newEmployee = new Employee(txtFName.getText() + " " + txtLName.getText(), txtAddress.getText(), txtNIC.getText(), txtPhone.getText(), txtDoB.getText(), post, txtGrade.getText(), txtDesignation.getText(), txtEmpID.getText(), userName, password);
        if (newEmployee.add() == 1)
        {
            JOptionPane.showMessageDialog(null, "Employee Inserted to the Database Successfully!", "Employee Database", JOptionPane.INFORMATION_MESSAGE);
            this.clearTextFields();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Employee Insertion Failed!", "Employee Database", JOptionPane.WARNING_MESSAGE);
        }
            
            
        
    }//GEN-LAST:event_btnAddEmpActionPerformed

