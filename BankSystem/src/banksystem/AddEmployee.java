    private void btnValidateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidateActionPerformed
        String password = String.valueOf(pswPass.getPassword());
        String passwordConfirm = String.valueOf(pswConfirmPass.getPassword());
        
        if (!(txtUsername.getText().equals("") || password.equals("") || passwordConfirm.equals("")))
        {
            if (!ValidateString.equality(password, passwordConfirm))
            {
                JOptionPane.showMessageDialog(null, "Passwords Don't Match!", "Invalid Password", JOptionPane.WARNING_MESSAGE);
                pswPass.setText("");
                pswConfirmPass.setText("");
                pswPass.requestFocus();
                btnAddEmp.setEnabled(false);
            }
            else
                btnAddEmp.setEnabled(true);            
        }
        
            
    }//GEN-LAST:event_btnValidateActionPerformed

    private void txtNICFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtNICFocusLost
        String nicNum = txtNIC.getText();
        if (!nicNum.equals(""))
        {
            if (!(ValidateString.length(nicNum, 10) && ValidateString.charAt(nicNum, 9, 'V')))
            {
                JOptionPane.showMessageDialog(null, "Invalid NIC Number.\nLength or Format is Invalid", "Invalid NIC", JOptionPane.WARNING_MESSAGE);
                txtNIC.setText("");
                txtNIC.requestFocus();
            }
        }
        
    }//GEN-LAST:event_txtNICFocusLost

    private void txtPhoneFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtPhoneFocusLost
        String phoneNum = txtPhone.getText();
        if (!phoneNum.equals(""))
        {
           if (!ValidateString.length(phoneNum, 10))
            {
                JOptionPane.showMessageDialog(null, "Invalid Phone Number.\nLength is Invalid", "Invalid Phone Number", JOptionPane.WARNING_MESSAGE);
                txtPhone.setText("");
                txtPhone.requestFocus();
            } 
        }
        
    }//GEN-LAST:event_txtPhoneFocusLost

