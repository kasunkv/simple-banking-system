    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String accNo = "";
        String accountNumber = "";
        String accountType = "";
        String accountStatus = "";
        
        
        float interest = 0;
        
        if ((accNo = txtUpdateAccNo.getText()).equals(""))
        {
            JOptionPane.showMessageDialog(null, "Enter an Account Number!", "Empty Field", JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            try {
                Connection dbConnect = DBConnection.connect();
                Statement dbQuery = dbConnect.createStatement();
                ResultSet result = dbQuery.executeQuery("SELECT * FROM accounts WHERE accNo = '"+accNo+"';");
                
                while(result.next())
                {
                    accountNumber = result.getString("accNo");
                    accountType = result.getString("accType");
                    accountBalance = result.getFloat("balance");
                    accountStatus = result.getString("accStatus");
                }
                if (accountStatus.equals("Deactive"))
                {
                    JOptionPane.showMessageDialog(null, "The Account is Closed/Deactivated.", "Closed Account", JOptionPane.INFORMATION_MESSAGE);
                    this.clearTextFields();
                }
                else
                {
                    if (accountNumber.equals(accNo))
                    {
                        txtAccBal.setText("Rs." + String.valueOf(accountBalance));
                        txtIntRate.setText(String.valueOf(evt));
                        if (accountType.equals("sav"))
                        {
                            try {
                                Statement dbQuery1 = dbConnect.createStatement();
                                ResultSet result1 = dbQuery1.executeQuery("SELECT intRate FROM savings WHERE accNo = '"+accNo+"';");

                                while(result1.next())
                                {
                                    interest = result1.getFloat("intRate");
                                }

                            }catch (Exception e) {
                                JOptionPane.showMessageDialog(null, "Error Message : " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                            }
                            txtAccType.setText("Savings Account");
                            txtIntRate.setText(String.valueOf(interest));
                            btnUpdate.setEnabled(true);
                            lblDisplay.setForeground(Color.GREEN);
                            lblDisplay.setText("Account Identified Successfully");

                        }
                        else if (accountType.equals("cur"))
                        {
                            try {
                                Statement dbQuery1 = dbConnect.createStatement();
                                ResultSet result1 = dbQuery1.executeQuery("SELECT odInt FROM current WHERE accNo = '"+accNo+"';");

                                while(result1.next())
                                {
                                    interest = result1.getFloat("odInt");
                                }

                            }catch (Exception e) {
                                JOptionPane.showMessageDialog(null, "Error Message : " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                            }
                            txtAccType.setText("Current Account");
                            txtIntRate.setText(String.valueOf(interest));
                            btnUpdate.setEnabled(true);
                            lblDisplay.setForeground(Color.GREEN);
                            lblDisplay.setText("Account Identified Successfully");

                        }
                    }
                    else
                    {
                        //JOptionPane.showMessageDialog(null, "Account Number doesn't match.\nAccount not in the Database.", "Account Not Found", JOptionPane.INFORMATION_MESSAGE);
                        lblDisplay.setForeground(Color.RED);
                        lblDisplay.setText("Account Not Found");
                        //this.clearTextFields();
                        txtUpdateAccNo.setText("");
                    }
                    
                }          
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error Reading From 'Account Database'.\nError Message : " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

