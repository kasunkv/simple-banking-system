    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String aType = txtAccType.getText();
        
        if (aType.equals("Savings Account"))
        {
            SavingsAccount newSA = new SavingsAccount();
            newAccountBalance = newSA.updateInterest(txtUpdateAccNo.getText(), accountBalance);
            txtNewBal.setText("Rs." + String.valueOf(newAccountBalance));
        }
        else if (aType.equals("Current Account"))
        {
            CurrentAccount newCA = new CurrentAccount();
            newAccountBalance = newCA.updateInterest(txtUpdateAccNo.getText(), accountBalance);
            if (newAccountBalance == accountBalance)
            {
                lblDisplay.setForeground(Color.YELLOW);
                lblDisplay.setText("Account Not Updated");
            }                
            txtNewBal.setText("Rs." + String.valueOf(newAccountBalance));
        }
            
    }//GEN-LAST:event_btnUpdateActionPerformed

