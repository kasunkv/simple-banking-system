    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            Connection dbConnect = DBConnection.connect();
            PreparedStatement dbUpdate = dbConnect.prepareStatement("UPDATE customer SET cusNIC = '"+txtNIC.getText()+"', name = '"+txtName.getText()+"', address = '"+txtAddress.getText()+"', phone = '"+txtPhone.getText()+"', dob = '"+txtDob.getText()+"' WHERE accNo = '"+txtAccNo.getText()+"';");
            dbUpdate.executeUpdate();
            dbUpdate.close();
            
            dbUpdate = dbConnect.prepareStatement("UPDATE accounts SET name = '"+txtName.getText()+"' WHERE accNo = '"+txtAccNo.getText()+"';");
            dbUpdate.executeUpdate();
            dbUpdate.close();
            
            JOptionPane.showMessageDialog(null, "Customer Details update Completed.", "Database Update", JOptionPane.INFORMATION_MESSAGE);
            this.clearTextFields();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error Updateing Database.\nError Message : " + e.getMessage(), "Database Update Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

