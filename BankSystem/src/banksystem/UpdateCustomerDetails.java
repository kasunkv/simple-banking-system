    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String accountNumber;
        String name = "";
        String adr = "";
        String nic = "";
        String dob = "";
        String phone = "";
        String AccNo = "";
        
        if (txtAccNo.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Enter an Account Number!.", "Account Number", JOptionPane.WARNING_MESSAGE);
            txtAccNo.requestFocus();
        }
        else
        {
            accountNumber = txtAccNo.getText();
            try {                
                Connection dbConnect = DBConnection.connect();
                Statement dbQuery = dbConnect.createStatement();
                ResultSet result = dbQuery.executeQuery("SELECT * FROM customer WHERE accNo = '"+accountNumber+"';");
                
                while(result.next())
                {
                    AccNo = result.getString("accNo");
                    name = result.getString("name");
                    adr = result.getString("address");
                    nic = result.getString("cusNIC");
                    dob = result.getString("dob");
                    phone = result.getString("phone");
                }
                
                if (AccNo.equals(accountNumber))
                {
                    txtAddress.setText(adr);
                    txtDob.setText(dob);
                    txtNIC.setText(nic);
                    txtName.setText(name);
                    txtPhone.setText(phone);
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Account not found in the Database", "Account Not Found", WIDTH);
                    this.clearTextFields();
                } 
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error Reading from the 'Customer Database'\nError Message : " + e.getMessage(), "Database Access Error!", JOptionPane.ERROR_MESSAGE);
            }
        }
}//GEN-LAST:event_btnSearchActionPerformed

